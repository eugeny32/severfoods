<h2 id="s4">4. Оффлайн-приложение</h2>

<h3 id="s41">4.1 Установка</h3>
<p><strong>Требования:</strong> Windows 10/11 x64, права пользователя (администратор <strong>не нужен</strong>).</p>
<ol>
<li>Скачайте <code>SeverFoods-Setup-X.X.X.exe</code> из раздела «Оффлайн».</li>
<li>Запустите установщик. Если приложение устанавливается впервые — введите <strong>токен синхронизации</strong> (выдаётся администратором в разделе «Точки питания» на сервере).</li>
<li>При необходимости измените папку установки → «Далее» → «Установить» → «Завершить».</li>
<li>Сразу после установки зайдите в «Настройки» и выставьте <strong>часовой пояс</strong> точки (раздел 3.4) — без этого статистика «сегодня» может считаться неверно.</li>
</ol>
<blockquote>База данных хранится в <code>%APPDATA%\SeverFoods\</code> — отдельно от программы и не удаляется при обновлении.</blockquote>

<h3 id="s42">4.2 Автообновление</h3>
<p>Начиная с версии 1.5.0 приложение обновляется <strong>само</strong>, без ручной переустановки:</p>
<ul>
<li>При наличии интернета приложение автоматически проверяет, вышла ли новая версия (сразу при запуске, затем раз в час).</li>
<li>Если версия новее — она тихо скачивается в фоне, не мешая работе оператора.</li>
<li>Установка (перезапуск приложения с новой версией) происходит автоматически, но только когда компьютер <strong>простаивает 5 минут и дольше</strong> (нет ввода с клавиатуры/мыши) — так обновление никогда не прервёт активное сканирование.</li>
</ul>
<p>Проверить статус или ускорить процесс можно в разделе <strong>«Настройки» → «Обновления»</strong>: там видна текущая версия, статус проверки/загрузки, кнопка «Проверить сейчас», а когда обновление уже скачано — кнопка «Установить сейчас» для немедленного перезапуска, не дожидаясь простоя. То же самое доступно через пункт «Проверить обновления» в меню трея.</p>
<blockquote>База данных, история питания и все настройки (часовой пояс, токен) сохраняются при автообновлении — обновляются только программные файлы.</blockquote>

<h3 id="s43">4.3 Обновление вручную (без сброса токена)</h3>
<p>Если по какой-то причине автообновление недоступно (например, компьютер долго не был в сети) — можно обновить вручную:</p>
<ol>
<li>Закройте приложение (трей → «Выход» или через диспетчер задач).</li>
<li>Запустите новый установщик поверх существующего.</li>
<li><strong>Страница ввода токена пропускается автоматически</strong> — текущий токен из <code>.env</code> сохраняется.</li>
<li>Нажмите «Далее» → «Установить» → «Завершить». База данных, история питания и настройка часового пояса сохранены.</li>
</ol>
<div class="warn">Никогда не удаляйте вручную папку <code>%APPDATA%\SeverFoods\</code> — там находится база данных. Деинсталлятор удаляет только программные файлы в <code>%LOCALAPPDATA%\SeverFoods\</code>.</div>

<h3 id="s44">4.4 Авторизация в приложении</h3>
<p>Авторизация выполняется сканированием QR-карты сотрудника или ручным вводом кода. Поле ввода автоматически транслитерирует кириллицу в латиницу — раскладка клавиатуры не влияет на результат.</p>

<h3 id="s45">4.5 Сканер и очередь QR</h3>
<p>При потоковом сканировании коды выстраиваются в <strong>очередь FIFO</strong> с интервалом обработки 250 мс — ни один скан не теряется даже при быстром считывании нескольких карт подряд.</p>
<table>
<tr><th>Цвет</th><th>Значение</th><th>Время показа</th></tr>
<tr><td><span class="badge badge-green">Зелёный</span></td><td>Успешный приём</td><td>3 сек</td></tr>
<tr><td><span class="badge badge-orange">Оранжевый</span></td><td>Повтор (уже питался сегодня)</td><td>3 сек</td></tr>
<tr><td><span class="badge badge-red">Красный</span></td><td>Отказ — звуковая тревога</td><td>8 сек + 5 сек звук</td></tr>
</table>
<ul>
<li>Красный баннер (отказ) не прерывается зелёным или оранжевым — оператор всегда видит ошибку.</li>
<li>Успешные и повторные сканы могут перекрываться между собой.</li>
<li>Проверка «уже питался сегодня» учитывает часовой пояс, настроенный в приложении (раздел 3.4).</li>
</ul>

<h3 id="s46">4.6 Синхронизация</h3>
<p>Выполняется автоматически по расписанию (раз в час), а также:</p>
<ul>
<li>Вручную — кнопка «Синхронизировать» в интерфейсе или через контекстное меню трея.</li>
<li>При восстановлении интернет-соединения (приложение работает полностью автономно при отсутствии связи).</li>
</ul>
<p>При синхронизации передаются новые записи о питании и обновляется база сотрудников с сервера. Накопленные офлайн-записи отправляются пакетами по 200 штук — это защищает от потери прогресса при обрыве медленного интернет-соединения: если связь прервётся на середине, уже переданные пакеты не отправляются повторно, досылаются только оставшиеся.</p>
<blockquote>Таймауты синхронизации рассчитаны на медленный канал: 45 секунд на обычные запросы и до 120 секунд на отправку большого объёма накопленных офлайн-записей. Если соединение действительно нестабильно, ошибка синхронизации может появляться периодически — это не потеря данных: неотправленные записи остаются в локальной базе и досылаются при следующей попытке.</blockquote>

