<p>Уведомления о новых сообщениях:</p>
<ul>
<li>Бейдж с числом непрочитанных на кнопке «Чат».</li>
<li>Всплывающий тост (нижний левый угол) с текстом сообщения.</li>
<li>Звуковой сигнал.</li>
</ul>

