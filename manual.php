<h2 id="s1">1. Общее описание</h2>
<p><strong>СеверФудс</strong> — система учёта питания сотрудников предприятия ООО «Север». Состоит из двух компонентов:</p>
<table>
<tr><th>Компонент</th><th>Назначение</th></tr>
<tr><td><strong>Онлайн</strong> (веб-приложение)</td><td>Центральный сервер: база данных сотрудников, отчёты, управление пользователями, чат, сухие пайки</td></tr>
<tr><td><strong>Оффлайн</strong> (приложение Windows)</td><td>Автономная точка питания: сканирование QR-карт, локальная БД, синхронизация с сервером</td></tr>
</table>
<p><strong>Принцип работы:</strong> администратор регистрирует сотрудников → печатает QR-карты → на точке питания сканируется карта → приложение фиксирует приём → данные синхронизируются с сервером → руководитель просматривает отчёты.</p>

<!-- ─────────────────────────────────────────── -->
<h2 id="s2">2. Онлайн-система</h2>

<h3 id="s21">2.1 Роли пользователей</h3>
<table>
<tr><th>Роль</th><th>Доступные разделы</th></tr>
<tr><td><strong>Оператор</strong></td><td>Главная (сканер QR), Чат</td></tr>
<tr><td><strong>Администратор</strong></td><td>Главная, Сотрудники, Отчёты, Чат, Печать QR, Оффлайн, Точки питания</td></tr>
<tr><td><strong>Супер-администратор</strong></td><td>Все разделы + управление пользователями и настройками системы</td></tr>
</table>

<h3 id="s22">2.2 Вход в систему</h3>
<p>Доступны два способа входа:</p>
<ul>
<li><strong>Логин / пароль</strong> — стандартная форма.</li>
<li><strong>QR-карта</strong> — поднесите карту к сканеру или камере. Поле ввода принимает коды независимо от раскладки клавиатуры (кириллица автоматически транслитерируется в латиницу).</li>
</ul>
<blockquote>При использовании USB-сканера не требуется переключение раскладки — система обрабатывает оба варианта.</blockquote>

<h3 id="s23">2.3 Сканер QR (главная страница)</h3>
<p>Основное рабочее место оператора. QR-сканер подключается как HID-клавиатура. Результаты сканирования:</p>
<table>
<tr><th>Цвет</th><th>Значение</th></tr>
<tr><td><span class="badge badge-green">Зелёный</span></td><td>Приём зафиксирован — отображаются ФИО и организация</td></tr>
<tr><td><span class="badge badge-orange">Оранжевый</span></td><td>Повторный приём — сотрудник уже питался сегодня</td></tr>
<tr><td><span class="badge badge-red">Красный</span></td><td>Отказ — сотрудник не найден, заблокирован или ошибка карты</td></tr>
</table>

<h3 id="s24">2.4 Сотрудники</h3>
<p>Управление базой сотрудников. Основные поля: ФИО, организация, подразделение, должность, Вахтовый жилой городок (ВЖГ), срок действия QR, статус (активен / заблокирован).</p>
<ul>
<li>QR-код генерируется автоматически при добавлении сотрудника.</li>
<li>Если дата рождения не указана — генерируется случайная (для совместимости форм).</li>
<li>Экспорт списка в Excel, печать QR-карт.</li>
</ul>

<h3 id="s25">2.5 Статистика питания сотрудника</h3>
<p>Открывается кнопкой
<svg style="display:inline;vertical-align:middle" width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="#003366" stroke-width="2.2"><line x1="18" y1="20" x2="18" y2="10"/><line x1="12" y1="20" x2="12" y2="4"/><line x1="6" y1="20" x2="6" y2="14"/></svg>
в списке сотрудников. Выводит данные за выбранный период:</p>
<table>
<tr><th>Карточка</th><th>Что показывает</th></tr>
<tr><td><strong>Приёмов пищи</strong></td><td>Общее число посещений столовой</td></tr>
<tr><td><strong>Дней в столовой</strong></td><td>Уникальные дни фактического питания в столовой</td></tr>
<tr><td><span class="badge badge-yellow">Сух. паёк / Выездн.</span></td><td>Число активных сухих пайков и выездных питаний (отображается отдельно, не входит в «Дней в столовой»)</td></tr>
</table>
<p>Ниже — разбивка по типам приёма (завтрак, обед, ужин, ночное).</p>

<h3 id="s26">2.6 Сухой паёк / Выездное питание</h3>
<p>Блок в модальном окне статистики сотрудника. Используется для учёта питания вне столовой.</p>

<h4>Добавление</h4>
<ol>
<li>Выберите тип: <span class="badge badge-blue">Выездное питание</span> или <span class="badge badge-yellow">Сухой паёк</span>.</li>
<li>Укажите диапазон дат (можно указывать будущие даты — заблаговременно на период командировки).</li>
<li>Лимит — <strong>4 записи</strong> активных за отображаемый период.</li>
</ol>

<h4>Статусы записей</h4>
<table>
<tr><th>Цвет</th><th>Статус</th><th>Условие</th></tr>
<tr><td><span class="badge badge-green">Зелёный</span></td><td>Активен (прошедшая дата)</td><td>Сотрудник был на выезде, дата миновала</td></tr>
<tr><td><span class="badge badge-orange">Оранжевый</span></td><td>Активен (будущая дата)</td><td>Запланировано, дата ещё не наступила</td></tr>
<tr><td><span class="badge badge-red">Красный</span></td><td>Аннулирован</td><td>Сотрудник пришёл в столовую в этот день — запись аннулируется автоматически</td></tr>
</table>

<blockquote>Сухие пайки и выездное питание учитываются <strong>отдельно</strong> от посещений столовой и не включаются в счётчик «Дней в столовой».</blockquote>

<h3 id="s27">2.7 Отчёты</h3>
<p>Раздел доступен администраторам. Два режима (переключатель «Раздел»):</p>

<h4>Режим «Столовая»</h4>
<p>Фильтры: период, тип приёма, точка питания. Содержит:</p>
<ul>
<li>Сводные карточки: всего питаний, завтраков, обедов, ужинов, ночных.</li>
<li>Графики по точкам и организациям.</li>
<li>Детальный журнал с сортировкой по любому столбцу (клик по заголовку).</li>
<li>Сводный отчёт по сотрудникам, сгруппированный по организациям.</li>
</ul>

<h4>Режим «Сухой паёк»</h4>
<p>Фильтр: период. Показывает все выданные сухие пайки и выездные питания по всем сотрудникам с разбивкой по статусу (активен / аннулирован). Поддерживает сортировку по столбцам.</p>

<h4>Экспорт в Excel</h4>
<table>
<tr><th>Кнопка</th><th>Содержимое</th></tr>
<tr><td><strong>Excel (детали)</strong></td><td>В режиме «Столовая» — журнал посещений; в режиме «Сухой паёк» — таблица пайков</td></tr>
<tr><td><strong>Excel (сотрудники)</strong></td><td>Сводная таблица с колонками «Приёмов пищи», «Дней в столовой», «Сух. паёк (дней)» + второй лист с детализацией пайков</td></tr>
</table>
<blockquote>Сортировка в таблицах: кликните по заголовку столбца — ▲ по возрастанию, ещё раз — ▼ по убыванию.</blockquote>

<h3 id="s28">2.8 Чат</h3>
<p>Корпоративный мессенджер. Типы комнат:</p>
<ul>
<li><strong>Личный диалог (DM)</strong> — между двумя пользователями.</li>
<li><strong>Группа</strong> — все участники могут писать и читать.</li>
<li><strong>Канал</strong> — для широковещательных сообщений; все участники могут писать.</li>
</ul>
<p>Уведомления о новых сообщениях:</p>
<ul>
<li>Бейдж с числом непрочитанных на кнопке «Чат».</li>
<li>Всплывающий тост (нижний левый угол) с текстом сообщения.</li>
<li>Звуковой сигнал.</li>
</ul>

<h3 id="s29">2.9 Оффлайн-раздел</h3>
<p>Вкладка «Оффлайн» в главном меню содержит:</p>
<ul>
<li>Ссылку для скачивания актуальной версии установщика Windows.</li>
<li>Ссылку на это справочное руководство.</li>
</ul>

