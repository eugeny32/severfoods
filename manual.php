<h2 id="s2">2. Онлайн-система</h2>

<h3 id="s21">2.1 Роли пользователей</h3>
<table>
<tr><th>Роль</th><th>Доступные разделы</th></tr>
<tr><td><strong>Оператор</strong></td><td>Главная (сканер QR), Чат</td></tr>
<tr><td><strong>Администратор</strong></td><td>Главная, Сотрудники, Отчёты, Чат, Печать QR, Оффлайн, Точки питания (только своя точка)</td></tr>
<tr><td><strong>Супер-администратор</strong></td><td>Все разделы + управление всеми точками питания, пользователями и настройками системы</td></tr>
</table>

<h3 id="s22">2.2 Вход в систему и сессии</h3>
<p>Доступны два способа входа:</p>
<ul>
<li><strong>Логин / пароль</strong> — стандартная форма.</li>
<li><strong>QR-карта</strong> — поднесите карту к сканеру или камере. Поле ввода принимает коды независимо от раскладки клавиатуры (кириллица автоматически транслитерируется в латиницу).</li>
</ul>
<blockquote>При использовании USB-сканера не требуется переключение раскладки — система обрабатывает оба варианта.</blockquote>
<p>Сессия пользователя удерживается до <strong>30 дней</strong> со скользящим продлением при каждом заходе — закрытие браузера или перезагрузка компьютера не приводят к разлогиниванию, пока сессия активно используется.</p>

<h3 id="s23">2.3 Сканер QR (главная страница)</h3>
<p>Основное рабочее место оператора. QR-сканер подключается как HID-клавиатура. Результаты сканирования:</p>
<table>
<tr><th>Цвет</th><th>Значение</th></tr>
<tr><td><span class="badge badge-green">Зелёный</span></td><td>Приём зафиксирован — отображаются ФИО и организация</td></tr>
<tr><td><span class="badge badge-orange">Оранжевый</span></td><td>Повторный приём — сотрудник уже питался сегодня</td></tr>
<tr><td><span class="badge badge-red">Красный</span></td><td>Отказ — сотрудник не найден, заблокирован или ошибка карты</td></tr>
</table>
<blockquote>Граница «сегодня» для проверки повторного приёма считается по местному времени <strong>той точки</strong>, на которой ведётся приём (см. раздел 3), а не по времени сервера.</blockquote>

<h3 id="s24">2.4 Сотрудники и поиск</h3>
<p>Управление базой сотрудников. Основные поля: ФИО, организация, подразделение, должность, Вахтовый жилой городок (ВЖГ), срок действия QR, статус (активен / заблокирован), роль.</p>
<ul>
<li>QR-код генерируется автоматически при добавлении сотрудника.</li>
<li>Если дата рождения не указана — генерируется случайная (для совместимости форм).</li>
<li>Экспорт списка в Excel, печать QR-карт.</li>
<li>Поле поиска распознаёт слова «<strong>оператор</strong>» / «<strong>администратор</strong>» (и производные) целиком — по вводу такого слова выпадает список всех операторов или администраторов соответственно.</li>
<li>В блоках организаций дополнительно выводятся отдельные счётчики-чипы «Операторы» и «Администраторы» — клик открывает список этой группы.</li>
</ul>

