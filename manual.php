<h2 id="s6">6. FAQ</h2>

<h4>Сотрудник потерял карту — что делать?</h4>
<p>Откройте карточку сотрудника → «Перевыпустить QR» → распечатайте новую карту. Старый код аннулируется после синхронизации на точках.</p>

<h4>Оффлайн-приложение не синхронизируется</h4>
<p>Проверьте интернет-соединение. Если соединение есть — откройте «Настройки» в приложении и убедитесь, что токен введён верно, URL сервера указан корректно. Попробуйте «Синхронизировать» вручную. На медленном интернете синхронизация может занимать до пары минут — это нормально, дождитесь завершения, не закрывая приложение.</p>

<h4>Счётчики «Завтрак / Обед / Ужин сегодня» на сайте и в оффлайн-приложении разные</h4>
<p>Почти всегда причина — разный часовой пояс, выставленный для точки на сайте и в самом приложении. Откройте раздел 3 этого руководства и сверьте оба значения — они должны совпадать буква в букву (например, оба +07:00).</p>

<h4>Красный баннер «Не найден в базе»</h4>
<p>Возможные причины: сотрудник ещё не добавлен в систему, или добавлен но синхронизация на точке не выполнялась, или сотрудник заблокирован.</p>

<h4>В отчёте «Сухой паёк» пусто</h4>
<p>Убедитесь, что в карточках сотрудников добавлены записи о сухих пайках, и что выбранный период в отчёте охватывает даты пайков. Пайки фильтруются по полю <code>ration_date</code>, а не по дате создания.</p>

<h4>Перенос приложения на другой компьютер</h4>
<p>Установите приложение на новый ПК с тем же токеном. Скопируйте файл <code>%APPDATA%\SeverFoods\severfoods.db</code> — локальная история питания восстановится. После переноса заново проверьте настройку часового пояса (раздел 3.4) — она хранится в этой же локальной базе, но лучше свериться с сайтом.</p>

<h4>Сортировка в таблицах отчётов сбилась</h4>
<p>Сортировка работает только на текущей странице и не сохраняется между фильтрациями. После нажатия «Применить» таблица вернётся к порядку по умолчанию (по дате убыванию). Кликните по нужному заголовку снова.</p>

<h4>Не все супер-администраторы могут добавить участника в общий чат</h4>
<p>Такого ограничения больше нет — любой пользователь с ролью супер-администратор может добавлять участников в любую группу или канал, независимо от того, кто её создал.</p>

<h4>Оффлайн-приложение долго не обновляется само</h4>
<p>Автообновление устанавливает новую версию только когда компьютер простаивает 5+ минут (нет ввода с клавиатуры/мыши) — на постоянно активной точке этот момент может не наступать долго. Откройте «Настройки» → «Обновления» → «Установить сейчас», чтобы обновиться немедленно, не дожидаясь простоя.</p>

<h4>При запуске ошибка <code>EPERM ... Program Files\SeverFoods\.env</code></h4>
<p>Устаревшая проблема, исправлена в версии 1.5.2: более ранние сборки по умолчанию ставили приложение в <code>C:\Program Files\</code>, куда обычный пользователь не может писать без прав администратора. Начиная с версии 1.5.2 установщик сам обнаруживает такую старую установку и тихо удаляет её перед установкой новой версии — достаточно просто установить актуальный <code>SeverFoods-Setup-X.X.X.exe</code> поверх, ничего вручную удалять не нужно. Новая версия ставится в <code>%LOCALAPPDATA%\Programs\SeverFoods\</code>, куда права администратора не нужны.</p>

