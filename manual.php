<h2 id="s3">3. Время и часовые пояса — подробно</h2>

<h3 id="s31">3.1 Почему это вообще важно</h3>
<p>Если точка питания находится не в московском часовом поясе, а часовой пояс для неё не настроен — статистика «сегодня» и отчёты будут <strong>искажены</strong>. Классический пример:</p>
<blockquote>
Точка питания работает по местному времени UTC+7 (например, Красноярск). Завтрак начинается в 06:30 <strong>по местному времени</strong> точки. Если система не знает, что точка в UTC+7, она может посчитать это время как 23:30 <strong>предыдущих</strong> суток — и приём пищи «утечёт» в отчёт за вчерашний день, а на сегодня счётчик покажет меньше, чем было на самом деле.
</blockquote>
<p>Похожая проблема возникает при несовпадении часового пояса между <strong>сайтом</strong> и <strong>оффлайн-приложением</strong> на компьютере точки: если на сайте для точки выставлен часовой пояс +07:00, а в самом оффлайн-приложении на компьютере эта настройка не сделана (используется значение по умолчанию, Москва +03:00) — счётчики «Завтрак / Обед / Ужин сегодня» на сайте и в оффлайн-программе будут расходиться на несколько часов проходов.</p>

<h3 id="s32">3.2 Как время хранится внутри системы</h3>
<p>Чтобы всё считалось предсказуемо независимо от того, из какого полушария смотрят на систему, применяется простое правило:</p>
<ul>
<li>В базе данных абсолютно всё время (проходы через QR, сухие пайки, сообщения чата) хранится <strong>строго в UTC+0</strong> — единое эталонное время без сдвигов.</li>
<li>«Местное» время — это отдельная настройка, которая применяется только при <strong>отображении</strong> и при <strong>подсчёте границы суток</strong> («что считать сегодняшним днём»).</li>
<li>У каждой точки питания может быть <strong>своя</strong> настройка часового пояса — так поддерживаются точки в разных регионах одновременно, без конфликта друг с другом.</li>
</ul>
<p>Отсюда следует главный практический вывод: <strong>часовой пояс нужно настроить в двух местах</strong> — на сайте (для точки питания) и в оффлайн-приложении (на компьютере этой же точки), и оба значения должны совпадать.</p>

<h3 id="s33">3.3 Настройка часового пояса точки на сайте</h3>
<ol>
<li><span class="step-num">1</span>Откройте раздел <strong>«Точки питания»</strong> (доступно администратору для своей точки, супер-администратору — для всех).</li>
<li><span class="step-num">2</span>У каждой точки в таблице есть колонка <strong>«Часовой пояс»</strong> с выпадающим списком UTC−12 … UTC+14.</li>
<li><span class="step-num">3</span>Выберите значение, соответствующее <strong>реальному физическому расположению</strong> точки питания (см. таблицу городов в разделе 3.6). Сохранение происходит автоматически при выборе значения.</li>
<li><span class="step-num">4</span>Если для точки ничего не выбрано («по умолчанию») — используется часовой пояс браузера того, кто сейчас смотрит статистику, что для точки в другом регионе даст <strong>неверный</strong> результат. Настройку рекомендуется выставлять явно для каждой точки, не в другом часовом поясе относительно Москвы.</li>
</ol>
<blockquote>После настройки все отчёты, статистика «сегодня» и время в журналах для этой точки будут считаться строго по её собственному часовому поясу, независимо от того, из какого города смотрит отчёт администратор.</blockquote>

<h3 id="s34">3.4 Настройка часового пояса в оффлайн-приложении</h3>
<p>Это <strong>отдельная</strong> настройка внутри самого Windows-приложения — она не подтягивается автоматически с сайта, её нужно выставить один раз на каждом компьютере точки.</p>
<ol>
<li><span class="step-num">1</span>Откройте оффлайн-приложение → раздел <strong>«Настройки»</strong>.</li>
<li><span class="step-num">2</span>Найдите карточку <strong>«Часовой пояс»</strong> (видна и редактируется ролями администратор / супер-администратор; оператору доступна только для просмотра).</li>
<li><span class="step-num">3</span>В выпадающем списке выберите UTC-офсет, <strong>совпадающий</strong> с тем, что выставлен для этой точки на сайте (шаг 3.3).</li>
<li><span class="step-num">4</span>Нажмите «Сохранить». Настройка применяется сразу — счётчики «Завтрак / Обед / Ужин / Ночное сегодня» и время в журнале сканирований пересчитаются по новому значению.</li>
</ol>
<div class="warn">Если часовой пояс на сайте и в оффлайн-приложении для одной и той же точки <strong>не совпадает</strong> — счётчики «сегодня» на сайте и в приложении будут отличаться, и синхронизированные записи могут отображаться под другой календарной датой на одной из сторон. Всегда выставляйте <strong>одинаковое</strong> значение в обоих местах.</div>
<p>По умолчанию (пока настройка не выставлена) используется UTC+03:00 (Москва).</p>

<h3 id="s35">3.5 Как проверить, что всё настроено правильно</h3>
<ol>
<li>Дождитесь начала рабочей смены на точке (например, начала завтрака).</li>
<li>Отсканируйте тестовый проход через оффлайн-приложение.</li>
<li>Откройте карточку «Сегодня» на сайте (для этой точки) и сравните счётчик с тем, что показывает оффлайн-приложение — числа должны совпадать.</li>
<li>Проверьте время прохода в журнале на сайте и в приложении — час и минута должны совпадать с реальным местным временем на точке.</li>
<li>Если время в журнале отличается от реального местного времени на точке — часовой пояс выставлен неверно; вернитесь к разделам 3.3/3.4 и исправьте значение.</li>
</ol>

