<h2 id="s4">4. Администрирование</h2>

<h3>Добавление сотрудника</h3>
<ol>
<li>Раздел «Сотрудники» → «Добавить».</li>
<li>Заполните ФИО, организацию и прочие поля → «Сохранить». QR-код генерируется автоматически.</li>
<li>Распечатайте QR-карту (кнопка «Печать QR»), выдайте сотруднику.</li>
<li>Выполните синхронизацию на оффлайн-точках — новый сотрудник появится там.</li>
</ol>

<h3>Назначение сухого пайка</h3>
<ol>
<li>Раздел «Сотрудники» → кнопка
<svg style="display:inline;vertical-align:middle" width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="#003366" stroke-width="2.2"><line x1="18" y1="20" x2="18" y2="10"/><line x1="12" y1="20" x2="12" y2="4"/><line x1="6" y1="20" x2="6" y2="14"/></svg>
рядом с сотрудником → вкладка выездного питания.</li>
<li>Выберите тип (Сухой паёк / Выездное питание), задайте диапазон дат.</li>
<li>Нажмите «Добавить». Лимит — 4 активных записи за период.</li>
</ol>
<blockquote>Если сотрудник с назначенным выездным питанием придёт в столовую — запись на этот день автоматически аннулируется.</blockquote>

<h3>Токен синхронизации</h3>
<p>Хранится в файле <code>%LOCALAPPDATA%\SeverFoods\.env</code> на рабочем месте. Один токен соответствует одной точке питания. При компрометации — перевыпустите токен в разделе «Точки питания» на сервере и обновите файл <code>.env</code> на точке.</p>

<h3>Добавление пользователя-оператора</h3>
<p>Раздел «Пользователи» (доступен супер-администратору) → «Добавить» → укажите роль «Оператор» и привяжите к точке питания.</p>

